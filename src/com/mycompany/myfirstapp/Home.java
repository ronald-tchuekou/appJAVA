    /**
     * Fonction qui permet de vérifier si une salle existe deja.
     * @param {String} code_salle Code de la salle.
     * @param {String} date_edition Date d'edition du programme.
     * @return {boolean} Booléen OUI oi NON.
     */
    public boolean isExist_Salle(String code_salle, String date_edition){
        try{
            conDB con = new conDB();
            data = con.connexion();
            
            String request = "select * from salle where code_salle = ?";
            
            // Péparation de la requête.
            stet = data.prepareStatement(request);
            stet.setString(1, code_salle);
  
            // Exécution de la requête.
            result = stet.executeQuery();
            
            // Fermeture de la connexion à la base de données;
            if(result.next()){
                stet.close();
                return true;
            }
                 
        }catch(SQLException e){
            System.out.println("Erreur de selection." + e.getMessage());
        }
        return false;
    }
    
