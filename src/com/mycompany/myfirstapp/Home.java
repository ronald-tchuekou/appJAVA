    // Cette fonction permet d'enregistrer un emploi du temps d'une salle.
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Message de confirmation.
        
        //Component frame = this;
        //JOptionPane.showMessageDialog(frame, "Message de confirmation !");
        setValues();
        if (this.validator()){
            if(isExist_Salle(code_Salle, date_edition))
                JOptionPane.showMessageDialog(null, "Cette salle existe deja ! ");
            else{
                addRoom();
                initAll();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Erreur: Indiquer le code de la salle.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

